package animal;

public class Animal {
//	The name property for the class
	public String name;

//	The constructor
	public Animal(String name) {
		super();
		this.name = name;
	}

//	The getters and setters of the class
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
